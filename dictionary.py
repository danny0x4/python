# list -> array, mengakses data dengan menggunakan index

data_list = ['bayu','kevin','ariq']

print(data_list[0])

# dictionary (dict) -> associative array
# identifier -> key

data_dict = {
    'key':'value',
    'by':'bayu',
    'kv':'kevin',
    'aq':'ariq',
    'nmbr':'100',
    'list':data_list
}

print(data_dict['kv'])
print(data_dict['nmbr'])
print(data_dict['list'])